    public ArrayList<Racuni> prikazListeRacun() {
        
    ArrayList<Racuni> prikazListeRacuna = new ArrayList<>();
    
        Connection konekcija = null;
        Statement stejtment = null;

        try{

            Class.forName("com.mysql.jdbc.Driver");
            
            konekcija = DriverManager.getConnection(server_databaze,korisnik_databaze,sifra_databaze);
            String prikazivanjeIzBaze = "SELECT * FROM racuni";
            stejtment = konekcija.createStatement();
            ResultSet rezultat = stejtment.executeQuery(prikazivanjeIzBaze);
            Racuni RacuniPrikaz;
            while(rezultat.next()){
                RacuniPrikaz = new Racuni(rezultat.getString("brojracuna"),rezultat.getString("nazivfajla"),rezultat.getString("datum"),rezultat.getString("vrijeme"),rezultat.getString("radnik"),rezultat.getString("smjena"));
                prikazListeRacuna.add(RacuniPrikaz);
            }
            }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
      
     return prikazListeRacuna;   
    }
    
    public void prikazivanjeRacunaUTabeli(){
        
        ArrayList<Racuni> lista = prikazListeRacun();
        DefaultTableModel ispisUTabeli = (DefaultTableModel)tabelaRacuna.getModel();
        Object[] red = new Object[6];
        for(int i=0;i<lista.size(); i++){
            
            red[0] = lista.get(i).getBrojRacuna();
            red[1] = lista.get(i).getNazivFajla();
            red[2] = lista.get(i).getDatum();
            red[3] = lista.get(i).getVrijeme();
            red[4] = lista.get(i).getRadnik();
            red[5] = lista.get(i).getSmjena();
            ispisUTabeli.addRow(red);
            
        }
        
    }
    
