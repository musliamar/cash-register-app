    private void otvoriRacunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_otvoriRacunActionPerformed

      TableModel pravljenjePredracuna = tabelaRacuna.getModel();
      
      int[] vrijednosti = tabelaRacuna.getSelectedRows();
      Object[] redovi = new Object[3];
      
      if(tabelaRacuna.getSelectionModel().isSelectionEmpty()){
          
            neuspjesnoDodavanjeNaRacun iskace = new neuspjesnoDodavanjeNaRacun();
            iskace.setVisible(true);
          
      }else{
      for(int i=0;i<vrijednosti.length; i++){
          
          
          redovi[0] = pravljenjePredracuna.getValueAt(vrijednosti[i], 0);
          
           if (Desktop.isDesktopSupported()) {
                    try {
                        File otvaranjeFajla = new File("Racun #" + redovi[0] + ".pdf");
                        Desktop.getDesktop().open(otvaranjeFajla);
                    } catch (IOException e) {
                        // ako ne bude moglo da se otvori
                    }
                }
          
          tabelaRacuna.getSelectionModel().clearSelection();
       
      }
      
      }

    }//GEN-LAST:event_otvoriRacunActionPerformed

